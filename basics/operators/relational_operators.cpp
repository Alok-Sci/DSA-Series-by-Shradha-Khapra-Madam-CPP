#include <iostream>
using namespace std;

// * Relational operators: returns boolean value (true--> 1; false--> 0)
// * greater than (>)
// * less than (<)
// * greater than or equal to (>=)
// * less than or equal to (<=)
// * equal to (==)
// * not equal to (!=)

int main() {
    cout << (3 > 5) << endl; // false
    cout << (3 < 5) << endl; // true
    cout << (3 <= 5) << endl; // true
    cout << (3 >= 5) << endl; // false
    cout << (3 == 5) << endl; // false
    cout << (3 != 5) << endl; // true
}